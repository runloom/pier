import { existsSync } from "node:fs";
import { mkdir, readFile, rm } from "node:fs/promises";
import { homedir } from "node:os";
import { dirname, join } from "node:path";
import type { AgentKind } from "@shared/contracts/agent.ts";
import { atomicWriteFile, commandExistsOnPath } from "./shared.ts";
import type { AgentHookIntegration } from "./types.ts";

const AGENT_ID: AgentKind = "kilo";

/** Pier йғЁзҪІзҡ„зӢ¬з«ӢжҸ’д»¶ж–Үд»¶пјҲдёҚж”ҫ kilo иҮӘиә«е®үиЈ…зӣ®еҪ•дёӢ, opencode еҗҢж¬ҫеёғеұҖпјүгҖӮ */
const PLUGIN_FILE = "pier-agent-status.ts";

/** жүҳз®Ўж Үи®°пјҡеҶҷеңЁжҸ’д»¶жәҗз ҒеҶ…, install е№ӮзӯүжҜ”еҜ№ + uninstall еҲ йҷӨеүҚеҝ…жҹҘгҖӮ */
const PLUGIN_MARKER = "pier-agent-status:v1 (managed by Pier)";

/**
 * Kilo Code CLI жҸ’д»¶ вҖ” дҫқжҚ®е®ҳж–№ж–ҮжЎЈ kilo.ai/docs/automate/extending/pluginsпјҡ
 * - жҸ’д»¶зӣ®еҪ•**иҮӘеҠЁеҠ иҪҪ**гҖҒж— йңҖ config жіЁеҶҢпјҡ`~/.config/kilo/plugin/` дёӢжҜҸдёӘ
 *   `.ts`/`.js` ж–Үд»¶еңЁеҗҜеҠЁж—¶иҮӘеҠЁжіЁеҶҢпјҲд№ҹеӯҳеңЁ `.kilo/plugin/` дёҺж—§зүҲ
 *   `.kilocode/plugin/` йЎ№зӣ®зә§зӣ®еҪ•, дҪҶ pier еҸӘиЈ…е…ЁеұҖзӣ®еҪ•, дёҺе…¶дҪҷгҖҢдёҖж¬Ў
 *   е®үиЈ…е…ЁеұҖз”ҹж•ҲгҖҚзҡ„йӣҶжҲҗзәӘеҫӢдёҖиҮҙпјүгҖӮ
 * - е®ҳж–№жңҖе°ҸжҸ’д»¶зӨәдҫӢзҡ„еҜјеҮәеҪўзҠ¶жҳҜ `export default { id, server }`пјҢ
 *   е…¶дёӯ `server` жҳҜ `Plugin` зұ»еһӢзҡ„е·ҘеҺӮеҮҪж•°
 *   `async (ctx) => ({ ...hook е®һзҺ°... })`вҖ”вҖ”дёҚжҳҜ prompt зҢңжөӢзҡ„
 *   `export default { event: async ({event}) => {...} }` зӣҙжҺҘеҪўзҠ¶пјҲйӮЈжҳҜ
 *   server е·ҘеҺӮ*иҝ”еӣһеҖј*йҮҢзҡ„дёҖдёӘ hook й”®, дёҚжҳҜйЎ¶еұӮеҜјеҮәпјүгҖӮжң¬ж–Үд»¶жҢүе®ҳж–№
 *   зңҹе®һеҪўзҠ¶з”ҹжҲҗжәҗз ҒгҖӮ
 * - дәӢд»¶жҖ»зәҝжҳ е°„пјҲEvents еҸӮиҖғйЎөзЎ®и®Өзҡ„дәӢд»¶еҗҚ, SOURCE иҜҒжҚ®пјүпјҡ
 *   session.createdвҶ’SessionStart, session.idleвҶ’Stop, session.errorвҶ’error,
 *   session.deletedвҶ’SessionEnd, session.status(busy/retry)вҶ’runningгҖҒ
 *   (idle)вҶ’StopпјҲkilo жҳҜ opencode fork, EventSessionStatus дәӢд»¶еҗҢжәҗ;
 *   Events еҸӮиҖғйЎө Session еҲҶзұ»жҳҺзЎ®еҲ—еҮә session.statusвҖ”вҖ”жЁЎеһӢ busy/retry
 *   еҝғи·іжҳҜжҜҸеӣһеҗҲеҝ…еҸ‘зҡ„ TURN_RESET, йҳІ Stop еҗҺе·Ҙе…·дәӢд»¶иў«ж°ёд№…еҗёж”¶;
 *   и§ҒиҒҡеҗҲеҷЁдёҚеҸҳејҸгҖҢжҳ е°„ Stop еҝ…йЎ»еҗҢж—¶жҳ е°„ TURN_RESETгҖҚпјүгҖӮ
 *   permission дәӢд»¶е®ҳж–№еҗҚ `permission.asked`/`permission.replied`
 *   пјҲEvents еҸӮиҖғйЎө Permission еҲҶзұ»жҳҺзЎ®еҲ—еҮә; opencode SDK йҮҢеҸ«
 *   permission.updated, kilo fork йҮҚе‘ҪеҗҚдәҶжӯӨдәӢд»¶, SOURCEпјүгҖӮ
 *   з”ЁжҲ·ж¶ҲжҒҜжҸҗдәӨдәӢд»¶пјҡEvents йЎөеҲ—жңү `command.executed`, дҪҶ payload
 *   еҪўзҠ¶жңӘзЎ®и®ӨжҳҜеҗҰеҗ« prompt.submit еҠЁдҪңвҖ”вҖ”жӢҝдёҚеҮҶзҡ„е®ҒзјәжҜӢж»Ҙ,
 *   дёҚиЈ… PromptSubmit; session.status(busy) е·ІжҸҗдҫӣ TURN_RESET,
 *   PromptSubmit зјәеӨұдёҚеҪұе“ҚжӯЈзЎ®жҖ§гҖӮ
 *   toolпјҡtool.execute.beforeвҶ’ToolStart, tool.execute.afterвҶ’ToolComplete
 *   пјҲдёҺ opencode йӣҶжҲҗеҗҢеҗҚдәӢд»¶, SOURCE зЎ®и®ӨдёҖиҮҙпјүгҖӮ
 * - emit з”Ё appendFileSyncпјҲpierAppend жЁЎжқҝ; Bun е®ҝдё»
 *   process.getBuiltinModule еҸҜз”Ё, ж—§ Node йҖҖеҢ–ејӮжӯҘ best-effortпјүгҖӮ
 *   ж— йЎ¶еұӮ import еЈ°жҳҺпјҲelectron-vite жЁЎжқҝеӯ—йқўйҮҸжү«жҸҸйҷ·йҳұпјүгҖӮ
 *   дёү PIER_ зҺҜеўғеҸҳйҮҸд»»дёҖзјәеӨұеҚійқҷй»ҳ no-op, еҗһејӮеёёгҖӮ
 * capability "full"гҖӮ
 */
export function kiloPluginPath(): string {
  return join(homedir(), ".config", "kilo", "plugin", PLUGIN_FILE);
}

export function buildKiloPluginSource(pluginId: AgentKind = AGENT_ID): string {
  return `// ${PLUGIN_MARKER}
// Do not edit; this file is regenerated by Pier and any changes may be lost.
// Deliberately no top-level import declarations: electron-vite scans
// template literals in main's bundle and can otherwise inject an invalid
// CommonJS shim into the ESM output. process.getBuiltinModule is a runtime
// call вҖ” not an ImportDeclaration вҖ” so the scan stays inert; available in
// Bun (kilo's host) and Node >= 20.16.

function pierAppend(log, line) {
	if (typeof process.getBuiltinModule === "function") {
		// еҗҢжӯҘеҶҷ:дҝқж–Үд»¶еәҸ + иҝӣзЁӢйҖҖеҮәеүҚиҗҪзӣҳ(Bun дёҺ Node >= 20.16)гҖӮ
		const { appendFileSync } = process.getBuiltinModule("node:fs");
		appendFileSync(log, line);
		return;
	}
	// ts-no-dynamic-import дҫӢеӨ–пјҡиҝҗиЎҢж—¶ import() йқһ ImportDeclaration,
	// ж—§ Node е®ҝдё»йҖҖеҢ–дёәејӮжӯҘ best-effort(дёҺж—§иЎҢдёәдёҖиҮҙ, дёҚжӣҙзіҹ)гҖӮ
	import("node:fs/promises")
		.then(({ appendFile }) => appendFile(log, line))
		.catch(() => {});
}

function pierEmit(pierEvent) {
	const log = process.env.PIER_AGENT_EVENT_LOG;
	const panelId = process.env.PIER_PANEL_ID;
	const windowId = process.env.PIER_WINDOW_ID;
	if (!log || !panelId || !windowId) return;
	const line = JSON.stringify({
		v: 1,
		kind: "agentEvent",
		ts: Date.now() * 1_000_000,
		panelId,
		windowId,
		pid: process.pid,
		agent: "${pluginId}",
		event: pierEvent,
	}) + "\\n";
	try {
		pierAppend(log, line);
	} catch {
		// best-effort, never throw into the agent's own event loop
	}
}

function mapPierEvent(event) {
	if (!event || typeof event.type !== "string") return null;
	if (event.type === "session.created") return "SessionStart";
	if (event.type === "session.idle") return "Stop";
	if (event.type === "session.error") return "error";
	if (event.type === "session.deleted") return "SessionEnd";
	if (event.type === "session.status") {
		// SDK EventSessionStatus: properties.status.type = busy/retry/idle
		// (kilo жҳҜ opencode fork, SDK дәӢд»¶еҗҢжәҗ; kilo.ai/docs/automate/extending/plugins
		// Events еҸӮиҖғйЎө Session еҲҶзұ»зЎ®и®Ө session.status)гҖӮ
		// busy/retry жҳҜжЁЎеһӢжҺЁиҝӣеҝғи·івҖ”вҖ”TURN_RESET, йҳІ Stop еҗҺе·Ҙе…·дәӢд»¶иў«еҗёж”¶
		// (иҒҡеҗҲеҷЁдёҚеҸҳејҸ:жҳ е°„ Stop зҡ„йӣҶжҲҗеҝ…йЎ»еҗҢж—¶жҳ е°„ TURN_RESET дәӢд»¶)гҖӮ
		const statusType =
			event.properties && event.properties.status && event.properties.status.type;
		if (statusType === "busy" || statusType === "retry") return "running";
		if (statusType === "idle") return "Stop";
		return null;
	}
	if (event.type === "permission.asked") return "PermissionRequest";
	if (event.type === "permission.replied") return "processing";
	return null;
}

const server = async () => {
	// session.created жҸҗдҫӣзңҹе®һ SessionStart дҝЎеҸ·, дёҚеҶҚеҗҲжҲҗгҖӮ
	return {
		event: async ({ event }) => {
			const mapped = mapPierEvent(event);
			if (mapped) pierEmit(mapped);
		},
		"tool.execute.before": async () => {
			pierEmit("ToolStart");
		},
		"tool.execute.after": async () => {
			pierEmit("ToolComplete");
		},
	};
};

export default { id: "${pluginId}-agent-status", server };
`;
}

function isManagedPlugin(content: string): boolean {
  return content.includes(PLUGIN_MARKER);
}

async function readPluginFile(path: string): Promise<string | null> {
  try {
    return await readFile(path, "utf8");
  } catch {
    return null;
  }
}

export async function installKiloHooks(
  pluginPath: string = kiloPluginPath()
): Promise<void> {
  const source = buildKiloPluginSource();
  const existing = await readPluginFile(pluginPath);
  if (existing !== null && !isManagedPlugin(existing)) {
    console.warn(
      `[agent-hooks:${AGENT_ID}] unmanaged plugin file present, skip install:`,
      pluginPath
    );
    return;
  }
  if (existing !== source) {
    await mkdir(dirname(pluginPath), { recursive: true });
    await atomicWriteFile(pluginPath, source);
  }
}

/**
 * uninstallпјҡжЈҖжҹҘ marker еҶҚеҲ , йқһжүҳз®Ўж–Үд»¶з»қдёҚеҲ йҷӨ, ж–Үд»¶дёҚеӯҳеңЁи§Ҷдёәе·ІеҚёиҪҪгҖӮ
 */
export async function uninstallKiloHooks(
  pluginPath: string = kiloPluginPath()
): Promise<void> {
  const existing = await readPluginFile(pluginPath);
  if (existing === null) {
    return;
  }
  if (!isManagedPlugin(existing)) {
    console.warn(
      `[agent-hooks:${AGENT_ID}] unmanaged plugin file present, skip uninstall:`,
      pluginPath
    );
    return;
  }
  await rm(pluginPath, { force: true });
}

function kiloDetect(): boolean {
  return (
    existsSync(join(homedir(), ".config", "kilo")) ||
    existsSync(join(homedir(), ".kilocode")) ||
    commandExistsOnPath("kilo")
  );
}

export const kiloIntegration: AgentHookIntegration = {
  capability: "full",
  detect: kiloDetect,
  id: AGENT_ID,
  install: () => installKiloHooks(),
  uninstall: () => uninstallKiloHooks(),
};

